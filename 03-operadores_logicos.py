# Operadores lógicos.
print("Operadores lógicos")
is_student = True
is_working = False
print("¿Es estudiante y trabaja?: " + str(is_student and is_working))
print("¿Es estudiante o trabaja?: " + str(is_student or is_working))
print("No es estudiante: " + str(not is_student))
print("No trabaja: " + str(not is_working))

# Operadores de comparación.
print("Operadores de comparación")
number_1 = 5
number_2 = 3
print("¿Número 1 es igual a número 2? " + str(number_1 == number_2))
print("¿Número 1 es distinto a número 2? " + str(number_1 != number_2))
print("¿Número 1 es mayor a número 2? " + str(number_1 > number_2))
print("¿Número 1 es menor a número 2? " + str(number_1 < number_2))
print("¿Número 1 es mayor o igual a número 2? " + str(number_1 >= number_2))
print("¿Número 1 es menor o igual a número 2? " + str(number_1 <= number_2))

